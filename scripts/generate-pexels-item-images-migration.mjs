#!/usr/bin/env node
/**
 * Fetches Pexels search (per_page=1) using each item's title as query,
 * takes photos[0].src.tiny, and writes a Supabase migration that sets
 * items.images = [{"url": "<tiny>", "order": 0}] (replaces array).
 *
 * Usage (from repo root):
 *   cd scripts && npm install
 *   export DATABASE_URL="postgresql://postgres:...@db....supabase.co:5432/postgres"
 *   export PEXELS_API_KEY="your_key"
 *   node generate-pexels-item-images-migration.mjs
 *
 * Options:
 *   --out=../supabase/migrations/20260329170000_item_images_pexels_by_title.sql
 *   --delay-ms=350        delay between Pexels calls (rate limits)
 *   --limit=100           max items (for testing)
 *
 * Pexels free tier is ~200 requests/hour; use --delay-ms 20000 for long runs.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import pg from 'pg';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

function parseArgs() {
  const out = process.argv.find((a) => a.startsWith('--out='))?.slice(6);
  const delayMs = Number(process.argv.find((a) => a.startsWith('--delay-ms='))?.slice(11) ?? 350);
  const limit = process.argv.find((a) => a.startsWith('--limit='))
    ? Number(process.argv.find((a) => a.startsWith('--limit=')).slice(8))
    : null;
  return {
    out:
      out ||
      path.join(
        __dirname,
        '../supabase/migrations/20260329170000_item_images_pexels_by_title.sql',
      ),
    delayMs: Number.isFinite(delayMs) ? delayMs : 350,
    limit: limit != null && Number.isFinite(limit) ? limit : null,
  };
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function pexelsTinyUrl(apiKey, query) {
  const u = new URL('https://api.pexels.com/v1/search');
  u.searchParams.set('query', query.trim().slice(0, 500));
  u.searchParams.set('per_page', '1');

  let res = await fetch(u, { headers: { Authorization: apiKey } });
  if (res.status === 429) {
    const retry = Number(res.headers.get('retry-after')) || 60;
    await sleep(retry * 1000);
    res = await fetch(u, { headers: { Authorization: apiKey } });
  }
  if (!res.ok) {
    const t = await res.text();
    throw new Error(`Pexels HTTP ${res.status}: ${t.slice(0, 200)}`);
  }
  const data = await res.json();
  return data.photos?.[0]?.src?.tiny ?? null;
}

function fallbackQuery(title) {
  const words = title.trim().split(/\s+/).filter(Boolean).slice(0, 5);
  if (words.length === 0) return 'object product photo';
  return words.join(' ');
}

async function main() {
  const { out, delayMs, limit } = parseArgs();
  const apiKey = process.env.PEXELS_API_KEY?.trim();
  const databaseUrl = process.env.DATABASE_URL?.trim();

  if (!apiKey) {
    console.error('Missing PEXELS_API_KEY');
    process.exit(1);
  }
  if (!databaseUrl) {
    console.error('Missing DATABASE_URL (Postgres connection string)');
    process.exit(1);
  }

  const client = new pg.Client({ connectionString: databaseUrl });
  await client.connect();

  let q = `SELECT id::text AS id, title FROM public.items ORDER BY created_at NULLS LAST, id`;
  if (limit != null) q += ` LIMIT ${Math.max(1, Math.floor(limit))}`;

  const { rows } = await client.query(q);
  await client.end();

  console.error(`Loaded ${rows.length} items from database`);

  const updates = [];
  let ok = 0;
  let skipped = 0;

  for (let i = 0; i < rows.length; i++) {
    const { id, title } = rows[i];
    let tiny = null;
    try {
      tiny = await pexelsTinyUrl(apiKey, title || 'swap item');
      if (!tiny) {
        tiny = await pexelsTinyUrl(apiKey, fallbackQuery(title || 'swap'));
      }
    } catch (e) {
      console.error(`Pexels error for ${id}: ${e.message}`);
      skipped++;
      await sleep(delayMs);
      continue;
    }

    if (!tiny) {
      console.error(`No photo for item ${id} (${(title || '').slice(0, 60)})`);
      skipped++;
      await sleep(delayMs);
      continue;
    }

    const payload = JSON.stringify([{ url: tiny, order: 0 }]);
    updates.push(
      `UPDATE public.items SET images = $pexels$${payload}$pexels$::jsonb, updated_at = now() WHERE id = '${id}'::uuid;`,
    );
    ok++;
    if ((i + 1) % 25 === 0) console.error(`Progress ${i + 1}/${rows.length}…`);
    await sleep(delayMs);
  }

  const header = `-- Generated by scripts/generate-pexels-item-images-migration.mjs
-- Replaces items.images with a single Pexels tiny image per listing title (search query = title).
-- Items skipped (API error / no results): ${skipped}
-- Items updated: ${ok}

BEGIN;

`;

  const footer = `
COMMIT;
`;

  fs.mkdirSync(path.dirname(out), { recursive: true });
  fs.writeFileSync(out, header + updates.join('\n') + footer, 'utf8');
  console.error(`Wrote ${out} (${ok} UPDATEs)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
